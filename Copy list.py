#Use the copy() method

thislist = ["apple","banana","cherry","orange","kiwi","melon","mango"]
mylist = thislist.copy()
print(mylist)

#Use the list() method

thislist = ["apple","banana","cherry","orange","kiwi","melon","mango"]
mylist = list(thislist)
print(mylist)

#Use the slice operator

thislist = ["apple","banana","cherry","orange","kiwi","melon","mango"]
mylist = thislist[:]
print(mylist)